        <h2 id="t-brasao">CARTA DO BRASÃO DE ARMAS DO COLÉGIO IMACULADA CONCEIÇÃO BRAZÓPOLIS – MG</h2>
        <div class="row justify-content-center">
            <div class="col-12 col-xl-4 col-lg-4 col-md-5 col-sm-6">
                <img class="img-fluid" alt="Brasao CIC" src="../img/brasao.png" />
            </div>
            <div id="txtBox" class="col-11 col-xl-6 col-lg-6 col-md-5 col-sm-6">
                <h5 class="sub-t">Descrição</h5>
                <p>Brasão institucional. Escudo lanceolado, de campo em prata, partido
                    de azul escuro, tendo como timbre uma coroa dourada de cinco pontas
                    emperoladas de diadema com pedras rubis e esmeraldas alternadas e
                    como listel uma faixa azul de fundo laranja, a qual contém o mote
                    “Colégio Imaculada Conceição” e, as extremidades direita e esquerda,
                    respectivamente o ano de 2000 e 2018. No campo em prata, na ponta,
                    uma montanha verde sombreada. No cume da montanha um livro aberto,
                    tendo à página esquerda o logotipo da instituição e à página
                    direita uma luneta apontada para a constelação de Cruzeiro do Sul,
                    em preto. No campo azul, brasona-se a flor-de-lis dourada
                    assentada sobre a lua crescente prateada, sendo o conjunto
                    cercado por doze estrelas de cinco pontas douradas.
                </p>
            </div>
        </div>
        <div class="row justify-content-center">
            <div id="txtBox" class="col-11">
                <h5 class="sub-t">Interpretação</h5>
                <p>
                    <ol>
                        <li>
                            <strong>Escudo lanceolado:</strong> formato utilizado na Idade Média e representa a ponta de uma
                            lança. Remete à defesa dos valores inerentes à Instituição à qual pertence o brasão.
                        </li>
                        <li>
                            <strong>Campo em prata e campo em azul:</strong> o metal prata é, heraldicamente, símbolo da virtude
                            teologal da fé e o esmalte azul a virtude cardeal da justiça, ambas presentes na
                            vida da Imaculada Virgem Maria.
                        </li>
                        <li>
                            <strong>A montanha:</strong> representada no esmalte verde, símbolo da virtude teologal da esperança,
                            indica a região da Serra da Mantiqueira, no sul de Minas Gerais, onde se
                            assenta o município no qual se localiza do Colégio Imaculada Conceição.
                        </li>
                        <li>
                            <strong>O livro aberto e os símbolos internos:</strong> o livro aberto representa a missão
                            de ensinardo colégio. Está aberto para simbolizar que todos têm acesso ao conhecimento
                            de todas as áreas (Humanas, Exatas, da Natureza e da Linguagem). O logotipo do
                            colégio foi valorizado e inserido como elemento brasonado na página do livro.
                            A luneta e a constelação do Cruzeiro do Sul representam o Observatório
                            Nacional em Brazópolis. Quer simbolizar também a pesquisa (científica)
                            e o olhar sempre voltado para o Cristo, para quem a Virgem Maria sempre aponta (luneta).
                        </li>
                        <li>
                            <strong> flor-de-lis, a lua e as estrelas:</strong> esse conjunto de elementos brasonados
                            formam as “armas da Imaculada Conceição”, simbolismo tirado do Apocalipse,
                            onde se vê uma mulher revestida de sol, tendo a lua debaixo dos pés e na
                            cabeça uma coroa de doze estrelas.
                        </li>
                        <li>
                            <strong>A coroa:</strong> o brasão tem por timbre uma coroa, representando a coroação da
                            Virgem Maria como rainha de todo o universo.
                        </li>
                        <li>
                            <strong>O listel:</strong> nas cores do logotipo do colégio (azul e laranja), traz
                            como mote o nome do colégio – Colégio Imaculada Conceição, - a data de
                            fundação do colégio (2000) e a data de confecção do brasão (2018).
                        </li>
                    </ol>
                </p>
