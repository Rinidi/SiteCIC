                <p>
                    No Ensino Fundamental (2ª fase) as unidades de ensino promovem aprendizagem e subsidiam o
                    desenvolvimento dos alunos. O processo de ensino-aprendizagem articulam fatos, conceitos,
                    princípios, valores, atitudes, normas e procedimentos. As ações educativas desenvolvem um
                    trabalho ativo e os alunos fazem descobertas, aprendem a pensar com o que já aprenderam. A
                    intencionalidade do processo ensino-aprendizagem aponta para o desenvolvimento da inteligência,
                    para a autonomia dos alunos resolverem problemas, para a maturidade social, que permita se
                    relacionarem mais plenamente com os outros. A 2ª fase do ensino fundamental privilegia os
                    aspectos tratados na fase anterior, aprofundando-os por meio do desenvolvimento de relações
                    entre conhecimentos, os quais surgem pela necessidade de dar respostas a situações
                    significativas de ensino. Assim a aprendizagem conecta com as experiências e o saberes dos
                    alunos que estimula o pensamento e que amplia as concepções sobre o mundo que os cerca.
                    Para isso, exploram a cultura como referência, os saberes das diversas áreas e os contextos
                    que dão sentido para as relações.
