                <p>
                    No ensino Médio os projetos educativos privilegiam os processos de elaboração de
                    significados, ou seja, visa proporcionar, além da base de cultura geral, uma
                    formação que possibilite ao educando interagir na comunidade, mediante a
                    preparação e orientação básica para o seu futuro ingresso na Instituição de
                    Ensino Superior e a vida universitária, bem como o mundo do trabalho, com as
                    competências que permitam acompanhar as mudanças que caracterizam a produção
                    no nosso tempo. Para isso, as situações de ensino enfatizam a atividade mental
                    dos alunos, orientando-os para o estabelecimento de relações e conexões entre
                    os seus conhecimentos, as experiências prévias e os conteúdos de aprendizagem.
                    Nessa mesma direção, é também finalidade do Ensino Médio o aprimoramento do
                    educando como pessoa humana, considerando sua formação ética e o desenvolvimento
                    da autonomia intelectual e do pensamento crítico. Tendo em vista a construção
                    de uma sociedade mais justa, ética, democrática, inclusiva, sustentável e solidária.
